    def obter_dados_preview(self):
        """Atualiza o cache de feições apenas se a camada ou o agrupamento mudar."""

        camada = self.dlg.mMapLayerComboBox.currentLayer()
        if not camada:
            self.preview_data_cache = None
            return None

        campo_atlas = self.dlg.combo_atlas.currentData()
        
        # CONDIÇÃO DE INVALIDAÇÃO:
        # Só buscamos as feições no QGIS se:
        # 1. O cache está vazio
        # 2. A camada selecionada mudou
        # 3. O campo de agrupamento (Atlas) mudou
        if (self.preview_data_cache is None or 
            self.ultimo_id_camada != camada.id() or 
            self.cache_val_atlas != campo_atlas):
            
            self.ultimo_id_camada = camada.id()
            self.cache_val_atlas = campo_atlas
            
            # Buscamos a primeira feição disponível (limit 1 para ser instantâneo)
            # Nota: Não usamos Subset aqui para ter a feição completa em cache
            request_amostra = QgsFeatureRequest().setLimit(1)
            sample_f = next(camada.getFeatures(request_amostra), None)
            
            if sample_f:
                if campo_atlas is None:
                    self.preview_data_cache = [sample_f]
                else:
                    # Se houver atlas, buscamos todas as feições do mesmo grupo
                    val_amostra = sample_f.attribute(campo_atlas)
                    filtro = f'"{campo_atlas}" = {QgsExpression.quotedValue(val_amostra)}'
                    request_filtrado = QgsFeatureRequest().setFilterExpression(filtro)
                    self.preview_data_cache = list(camada.getFeatures(request_filtrado))
            else:
                self.preview_data_cache = None
        
        return self.preview_data_cache
    
    
    def atualizar_preview(self):
        """Renders a sample layout preview usando cache de feições."""

        if not self.dlg or sip.isdeleted(self.dlg):
            return        

        if self.is_rendering or not self.dlg.isVisible(): 
            return
        
        self.is_rendering = True
        self.dlg.previewProgressBar.show() 
        self.dlg.previewProgressBar.setRange(0, 0) 
        QCoreApplication.processEvents()

        self.limpar_clones_preview()

        layout = None
        try:
            # 1. Recupera feições da memória (instantâneo se já carregadas)
            feicoes = self.obter_dados_preview()
            camada = self.dlg.mMapLayerComboBox.currentLayer()

            if not feicoes or not camada:
                self.dlg.lbl_preview.clear()
                return

            # 2. Prepara o Layout
            layout = QgsPrintLayout(QgsProject.instance())
            layout.initializeDefaults()
            
            # 3. Monta o design (Lógica estritamente visual)
            self.montar_design_da_pagina(
                layout, 
                camada, 
                feicoes, 
                self.dlg.combo_presets.currentData(),
                "Retrato" if self.dlg.rb_retrato.isChecked() else "Paisagem",
                is_preview=True
            )

            # 4. Exporta para imagem
            exporter = QgsLayoutExporter(layout)
            image = exporter.renderPageToImage(0)
            if not image.isNull():
                self.dlg.lbl_preview.setPixmap(QPixmap.fromImage(image).scaled(
                    self.dlg.lbl_preview.size(), 
                    Qt.AspectRatioMode.KeepAspectRatio, 
                    Qt.TransformationMode.SmoothTransformation
                ))
        
        finally:
            if layout:
                layout.pageCollection().clear()
                sip.delete(layout)
            
            self.limpar_clones_preview()

            self.is_rendering = False
            self.dlg.previewProgressBar.hide()
            self.dlg.previewProgressBar.setRange(0, 100)
            QCoreApplication.processEvents()


