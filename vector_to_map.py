    def criar_camada_temporaria(self, camada_original, feicoes, nome_pagina, is_preview=False):
        """Cria uma memory layer. Se for preview, faz oculto. Se for final, joga numa pasta acima da original."""
                        
        crs = camada_original.crs().authid()
        geom_type = camada_original.wkbType()
        geom_string = QgsWkbTypes.displayString(geom_type)
        
        uri = f"{geom_string}?crs={crs}"
        camada_temp = QgsVectorLayer(uri, f"Mapa_{nome_pagina}", "memory")

        # --- NOVO: A BLINDAGEM DO CRS ---
        # Força a cópia exata do CRS, mesmo que a camada original não tenha código EPSG
        camada_temp.setCrs(camada_original.crs())
        
        provider = camada_temp.dataProvider()
        provider.addAttributes(camada_original.fields())
        camada_temp.updateFields()
        
        provider.addFeatures(feicoes)
        camada_temp.updateExtents() 
        
        # Copia o Renderer (cores, preenchimentos)
        camada_temp.setRenderer(camada_original.renderer().clone())
        
        # Opcional: Copia os rótulos também, se existirem
        if camada_original.labelsEnabled():
            camada_temp.setLabeling(camada_original.labeling().clone())
            camada_temp.setLabelsEnabled(True)
        
        if is_preview:
            # PREVIEW: O QGIS carrega na memória do projeto, mas esconde da legenda
            QgsProject.instance().addMapLayer(camada_temp, False)
        else:
            # MAPA FINAL: Cria um grupo posicionado imediatamente acima da camada original
            root = QgsProject.instance().layerTreeRoot()
            grupo = root.findGroup("Temp - VectorToMap")
            
            if not grupo:
                # 1. Encontra o "nó" da camada original na árvore do QGIS
                no_camada_original = root.findLayer(camada_original.id())
                
                if no_camada_original:
                    # 2. Descobre quem é o "pai" (pode ser a raiz ou uma pasta existente)
                    no_pai = no_camada_original.parent()
                    
                    # 3. Descobre a posição (índice) exata da camada original dentro desse pai
                    idx_insercao = 0
                    for i, filho in enumerate(no_pai.children()):
                        if filho == no_camada_original:
                            idx_insercao = i
                            break
                            
                    # 4. Insere o grupo nessa mesma posição, empurrando a original para baixo
                    grupo = no_pai.insertGroup(idx_insercao, "Temp - VectorToMap")
                else:
                    # Fallback de segurança caso a camada original não seja encontrada
                    grupo = root.insertGroup(0, "Temp - VectorToMap")
            
            # Adiciona a camada de forma invisível à raiz, e depois move para o nosso grupo
            QgsProject.instance().addMapLayer(camada_temp, False) 
            grupo.addLayer(camada_temp)
            
        return camada_temp


